class ArrayEx1
{
  public static void main(String args[])
  {
    int x[]={45,77,32,85,56,72,46,88,54,24,76};
    for(int c=0;c<x.length;c++)
    {
      System.out.println(x[c]);
    }
    System.out.println("number of elements in x "+x.length);

    double y[]={4.7, 6.3, 8.6, 9.3};
    for(int c=0;c<y.length;c++)
    {
      System.out.println(y[c]);
    }
    System.out.println("number of elements in y "+y.length);
  }
}
