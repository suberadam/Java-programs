// java.lang.Math class functions
class MathFunctionsEx1
{
  public static void main(String args[])
  {
     System.out.println(Math.E);
     double p=Math.PI;
     System.out.println(p);
     System.out.println(Math.sin(0));
     System.out.println(Math.toRadians(180));
     System.out.println(Math.toDegrees(p));
     System.out.println(Math.ceil(6.8));
     System.out.println(Math.ceil(6.2));
     System.out.println(Math.floor(6.8));
     System.out.println(Math.floor(6.2));

  }
}
